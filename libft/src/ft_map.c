#include <stdlib.h>

int		*ft_map(int *tab, int lenght, int (*f)(int))
{
	int i;
	int *ptr;

	i = 0;
	ptr = (int*)malloc(sizeof(int) * lenght);
	while (i < lenght)
	{
		ptr[i] = f(tab[i]);
		i++;
	}
	return (ptr);
}
